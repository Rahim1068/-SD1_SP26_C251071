package sd_lab_report_1_java_operators_c251071;

public class SD_Lab_Report_1_Java_Operators_C251071 {
    public static void main(String[] args) {
        
        int x = 13;
        int y = 4;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

        int z = 8;
        ++z;
        System.out.println(z);
        --z;
        System.out.println(z);
        
    }
}
